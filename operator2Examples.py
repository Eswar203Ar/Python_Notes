# User input using Operators Example
num1 = int(input("Enter num1 value : "))
num2 = int(input("Enter num2 value : "))
print(f"num1 + num2 = {num1+num2}")
print(f"num1 - num2 = {num1-num2}")
print(f"num1 * num2 = {num1*num2}")
print(f"num1 / num2 = {num1/num2}")
print(f"num1 // num2 = {num1//num2}")
print(f"num1 ** num2 = {num1**num2}")
